function closeSidebar() {
    document.getElementById("sidebar-map-container-id").style.width = "0";
    document.getElementById("sidebar-map-container-id-question-type").style.width = "0";
}

function openSidebar() {
    document.getElementById("sidebar-map-container-id").style.width = "30%";
    document.getElementById("sidebar-map-container-id-question-type").style.width = "30%";
}
